public class zmienne {

    public static void main(String[] args) {
        boolean bool = false;
        char c = 'g';
        int i = 2123456789;
        long l = 5123456789L;
        float f = 3.14f;
        double d = 3.1415;
        String s = "wartość_stringa";
        System.out.println("Wartość zmiennej boolean: " + bool + "\n" +
                "Wartość zmiennej char: " + c + "\n" +
                "Wartość zmiennej int: " + i + "\n" +
                "Wartość zmiennej long: " + l + "\n" +
                "Wartość zmiennej float: " + f + "\n" +
                "Wartość zmiennej double: " + d + "\n" +
                "Wartość zmiennej String: " + s + "\n"
                );
    }
}
